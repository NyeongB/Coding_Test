package BOJ_Basic;

import java.util.Scanner;

public class BOJ_Basic_06
{
	public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        
        int n = sc.nextInt();
        int m = sc.nextInt();
        
        System.out.println(n * m - 1);
    }
}
